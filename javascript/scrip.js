/*TL12 Desenvolvimento da abertura e fechamento do menu lateral*/
/*TL12.1 Abertuda*/
function abrirNav(){
    document.getElementById("menu-oculto").style.width="250px";
    document.getElementById("").style.marginLeft="250px";
}
/*TL12.2 Fechamento*/
function fecharNav(){
    document.getElementById("menu-oculto").style.width="0px";
    document.getElementById("").style.marginLeft="0px";    
}